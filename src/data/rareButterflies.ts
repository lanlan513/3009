export function getRareButterflyById(id: string): RareButterfly | undefined {
  return rareButterflies.find((b) => b.id === id);
}

export function getRareButterfliesByStatus(status: ConservationStatus): RareButterfly[] {
  return rareButterflies.filter((b) => b.conservationStatus === status);
}

export function getRareButterfliesByContinent(continent: string): RareButterfly[] {
  return rareButterflies.filter((b) =>
    b.distributionRegions.some((r) => r.continent === continent)
  );
}

export function getEndemicButterflies(): RareButterfly[] {
  return rareButterflies.filter((b) => b.isEndemic);
}

