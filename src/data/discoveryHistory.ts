export function getEventsByEra(eraId: string): DiscoveryEvent[] {
  const era = eras.find((e) => e.id === eraId);
  if (!era) return [];
  return discoveryEvents.filter((e) => e.year >= era.startYear && e.year <= era.endYear);
}

export function getEventById(id: string): DiscoveryEvent | undefined {
  return discoveryEvents.find((e) => e.id === id);
}

export function getEventsByCategory(category: DiscoveryEvent["category"]): DiscoveryEvent[] {
  return discoveryEvents.filter((e) => e.category === category);
}

export function getEventsByContinent(continent: string): DiscoveryEvent[] {
  return discoveryEvents.filter((e) => e.location.continent === continent);
}

